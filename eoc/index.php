          <h3>EQUAL OPPORTUNITY CELL</h3>
          <p>The Equal Opportunity Cell of Avinashilingam Institute for Home Science and Higher Education for Women was constituted by the Institute on 6th January 2010. The Cell was set up to implement and monitor the functions and activities of welfare of the disadvantaged and marginalized groups of the society and other related activities organised to foster academic and professional skill competency among college students.</p>
          <p>The Equal Opportunity Cell with the main objectives to oversee the effective implementation of Policies and Programme for disadvantages groups of SC/ST and Minority Communities, and provides an academic platform to guide, mould, Counsel and to create opportunities to students to excel in their Professional Career without discrimination. The Institute undertakes various activities with the Co-ordinated efforts of the following factories</p>
          <h3>Equal Opportunity Cell</h3>
          <p>The cell regularly enrols the students for all the courses from the afore mentioned categories to help the students to enhance the employability skills by means of training and give awareness to competitive exams, Placement, National and State Eligibility Test for Lecturer ship, Carreer Guidance and Remedial Training. The cell also offers in house training and remedial caching to the students with the help of experienced faculty.</p>
          <h3>SC/ST Cell</h3>
          <p>The SC/ST cell functions actively with its own significant dynamics. The Cell strive to channelize benefits of the Government Schemes to the students. Besides guiding the students on career options and opportunities the centre also motivate the students to update and equip themselves to face the academic the challenges.</p>
          <h3>Units for Differently Abled Persons</h3>
          <p>The scheme caters to the educational needs of students with different disabilities studying in different programmes. The students are being trained in the use of high technology for their convenient learning. Inclusive environment is being provided in the classroom and the hostel attached to the campus. To function as a unit to redress grievances of differently-abled students and render them necessary help in solving their academic and functional problems.</p>
          <p>Besides offering in -house training and remedial caching to the students with the help of experienced faculty, the cell also utilize the fund for the benefit of the students and hold review meetings to monitor and improve the performance level of the students Thus the EOC provides a comfortable learning environment by removing infrastructural pitfalls for the students with disabilities and enables access to resources (special assistive devices) and to various courses. The cell takes genuine effort to benefit the afore mentioned students through scholarships and other government policies</p>
